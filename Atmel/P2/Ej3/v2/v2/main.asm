start:
	   LDI R31, 0X01	;PRIMER BLOQUE
	   LDI R30, 0X00	;Z =0X100
	   LDI R28, 0X00	;SEGUNDO BLOQUE
	   LDI R29, 0X02	;Y =0X200
CICLO: CPI R28, 0X9A  ;CICLO HASTA PARTE BAJA
	   BREQ FIN       ;TERMINA CICLO POR CONDICION
	   LD R16,Z+		;CARGA Z EN R16, DEESPUES INCREMENTA
	   ST Y+, R16   ;GUARDA EN SEGUNDO BLOQUE, DESPUES INCREMENTA
	   rjmp CICLO     ;TERMINA CICLO, REINICIA
FIN:   rjmp FIN
rjmp start