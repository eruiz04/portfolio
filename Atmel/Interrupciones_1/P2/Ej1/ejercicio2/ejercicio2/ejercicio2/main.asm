;SUMAR EL CONTENIDO DE LAS LOCALIDADES 0X100 A 0X199 USANDO CICLOS Y DIRECCIONAMIENTO INDEXADO

LDI R30,0X00			;PARTE BAJA REG Z
LDI R31,0X01			;PARTE ALTA REG Z
CLR R16					;ALMACENA RESULTADOS PARCIALES	0X10 EN IREG
CLR R17					;ALMACENA VALOR Z

CICLO:
CPI R30,0XFB		;0X199+1
BREQ FIN
LD R17,Z+			;CARGA VALOR Z EN R17, DESPUES INCREMENTA
ADD R16,R17			;SUMA PARICAL MAS VALOR ACTUAL
CLR R17				;PONE EN 0 DE NUEVO 
RJMP CICLO

FIN:
RJMP FIN